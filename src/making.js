function Making(prob) {
  let arr = [];
  let randIdx = Math.floor(Math.random() * 4);
  for (let i = 0; i < 9; i++) {
    let temp = [];
    for (let j = 0; j < 9; j++) {
      let randProb = Math.floor(Math.random() * 6);
      temp.push({
        idx: [i, j],
        value: randProb < prob ? 0 : questions[randIdx][i][j],
        isBlank: randProb < prob ? true : false,
      });
    }
    arr.push(temp);
  }
  return [arr, questions[randIdx]];
}
